          <h4 itemprop="headline" class="treatment__title">Что означает профиль лечения и для кого подходит?</h4>
          <p itemprop="articleBody" class="treatment__text">
            Заболевания нервной системы – одни из самых распространенных заболеваний, как среди взрослых, так и детей. В связи с этим логично предположить, что санатории с неврологическим профилем
            пользуются большой популярностью для лечения и поддержания безболезненного состояния. Перечень заболеваний перифирийной и центральных нервных систем лечат во многих здравницах, главное
            условия - не острое состояние больного. В санаториях Урала и Башкирии применяются современные методики и профессиональное лечения согласно медицинских лицензий курортов. Также в санаториях
            разработано много антистрессовых программ, направленных на лечение нервозов, заболеваний вегетативной нервной системы, хронической усталости и нормализации сна.
          </p>
          <p itemprop="articleBody" class="treatment__text">
            Лечение в санатории представляет собой комплекс процедур вкупе с правильным режимом, направленном на нормализацию сна и бодрствования, сбалансированным питанием, физическими нагрузками,
            такими как терренкур в экологически чистой местности, посещение бассейна или лечебной физкультуры. Лечение назначается и расписывается на весь период нахождения в здравнице лечащим врачом
            санатория, руководствуется он текущим состоянием отдыхающего и данными санаторно-курортной карты (ее обязательно оформить до приезда в санаторий в поликлинике, в нем должен быть указан
            диагноз и все результаты анализов).
          </p>
