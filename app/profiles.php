              (гипертоническая болезнь); могут быть обусловлены врожденным пороком развития, травмой, воспалительным процессом и другими.
            </p>
            <p itemprop="description" class="profile-item__text">
              Лечением сердечно-сосудистых заболеваний занимается врач-кардиолог, к которому необходимо обращаться при малейших признаках заболевания сердца или сосудов, потому что общей чертой
              практически всех сердечно-сосудистых заболеваний является прогрессирующий характер болезни. При подозрениях на проблемы с сердцем нельзя дожидаться видимых симптомов, очень многие
              заболевания сердечно-сосудистой системы начинаются с появления у больного субъективного ощущения, что «что-то не так». Чем более раннюю стадию болезни выявит при осмотре врач-кардиолог,
              тем легче, безопаснее и с меньшим количеством медикаментов пройдет лечение. Болезнь часто развивается совершенного незаметно для больного, и отклонения от нормы возможно заметить только
              при обследовании кардиологом. Поэтому профилактические визиты к врачу-кардиологу с обязательным исследованием ЭКГ необходимы хотя бы один раз в год.
